# sorted function
numbers = [9,2,6,1,4,3]
print(sorted(numbers))

# reverse method
numbers.reverse()
print(numbers)

# clear method
numbers.clear()
print(numbers)

# copy method
numbers = [9,2,6,1,4,3]
new_numbers = numbers.copy()
print(new_numbers)

