# insert method 
fruits1= ['mango', 'orange']
fruits1.insert(0,'grapes')
print(fruits1)

# how to join(concatenate) two list
fruits2= ['grapes', 'apple']
fruits = fruits1 + fruits2
print(fruits)

# extend method
fruits1.extend(fruits2)
print(fruits1)
print(fruits2)

# append methos (list in list {nested list})
fruits1.append(fruits2)
print(fruits1)

