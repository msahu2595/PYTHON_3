# compare lists
# == and is

fruits1 = ['orange', 'apple', 'pear']
fruits2 = ['orange', 'apple', 'pear']
fruits3 = ['banana', 'kiwi', 'apple']
fruits4 = fruits1

print(fruits1 == fruits2)
print(fruits1 is fruits2)
print(fruits1 == fruits3)
print(fruits1 is fruits3)
print(fruits1 == fruits4)
print(fruits1 is fruits4)


