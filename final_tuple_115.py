# something more about tuples, list, str

# make tuple from range function
nums = tuple(range(1,11))
print(nums)
print(type(nums))

# convert tuple in list
num_1 = list(nums)
print(num_1)
print(type(num_1))

# convert tuple in string
num_2 = str(nums)
print(num_2)
print(type(num_2))

# convert list in string
num_3 = str(num_1)
print(num_3)
print(type(num_3))

