# common methods to delete data from list


# pop method

fruits =  ['orange', 'apple', 'pear', 'banana', 'apple', 'kiwi']
fruits.pop(2)
print(fruits)

# del statement/method

fruits =  ['orange', 'apple', 'pear', 'banana', 'apple', 'kiwi']
del fruits[1]
print(fruits)

# remove

fruits =  ['orange', 'apple', 'pear', 'banana', 'apple', 'kiwi']
fruits.remove('apple')
print(fruits)

